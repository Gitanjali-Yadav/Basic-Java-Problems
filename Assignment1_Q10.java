// Write a Java program to display the multiplication table for a given number using a
// while loop.


public class Assignment1_Q10 {
    public static void main(String[] args) {
        
        int i=1, n=5;
        while(i<=10){
            System.out.println(n + " * " + i + " = " + i*n);
            i++;
        }
    }
}
